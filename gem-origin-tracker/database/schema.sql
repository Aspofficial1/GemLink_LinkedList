-- ---------------------------------------------------------
-- Indexes
-- ---------------------------------------------------------
-- Indexes are created on frequently searched columns to speed
-- up queries. Without indexes, every search would scan the
-- entire table row by row, which is slow for large datasets.
-- ---------------------------------------------------------

-- Index on gem_stages.gem_id to speed up loading a gem's full journey
CREATE INDEX IF NOT EXISTS idx_gem_stages_gem_id
    ON gem_stages(gem_id);

-- Index on gem_stages.stage_order to speed up ordering the chain
CREATE INDEX IF NOT EXISTS idx_gem_stages_order
    ON gem_stages(gem_id, stage_order);

-- Index on gems.gem_type to speed up searching gems by type
CREATE INDEX IF NOT EXISTS idx_gems_type
    ON gems(gem_type);

-- Index on gem_alerts.gem_id to speed up loading alerts for a gem
CREATE INDEX IF NOT EXISTS idx_alerts_gem_id
    ON gem_alerts(gem_id);

-- Index on gem_alerts.is_resolved to quickly find unresolved alerts
CREATE INDEX IF NOT EXISTS idx_alerts_resolved
    ON gem_alerts(is_resolved);